<script>
    // Obtiene todos los productos al cargar la página
    var productos = <?php echo json_encode($productos); ?>;

    // Agrega un listener para el evento 'input' en el campo de búsqueda
    document.getElementById('buscador').addEventListener('input', function() {
        // Obtiene el valor del campo de búsqueda
        var searchTerm = this.value.toLowerCase();

        // Filtra los productos que coinciden con el término de búsqueda
        var filteredProductos = productos.filter(function(producto) {
            return producto.descripcion_producto.toLowerCase().includes(searchTerm);
        });

        // Limpia el contenedor de productos
        var contenedorItems = document.querySelector('.contenedor-items');
        contenedorItems.innerHTML = '';

        // Renderiza los productos filtrados
        filteredProductos.forEach(function(value) {
            // ... (resto del código para renderizar los productos)
            var nuevoItem = document.createElement('div');
            nuevoItem.className = 'item';
            nuevoItem.innerHTML = '<span class="titulo-item">' + value.descripcion_producto + '</span>';
            contenedorItems.appendChild(nuevoItem);
        });
    });
</script>